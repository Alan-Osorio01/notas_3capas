    // Método para listar los artículos en la tabla #1
    public void ListarArticulos(){
        DefaultTableModel tabla = new DefaultTableModel();
        DatoNotas objart = new DatoNotas();
        ArrayList<DatoNotas> lista2 = new ArrayList();
        lista2 = objart.ListaDatosBasicos();
        tabla.addColumn("Codigo");
        tabla.addColumn("ID");
        tabla.addColumn("Nombre");
        tabla.addColumn("Correo");
        tabla.addColumn("numero");
        tabla.addColumn("Semestre");
        tabla.setRowCount(lista2.size());
        int i = 0;
        for (DatoNotas x : lista2) {
            tabla.setValueAt(x.getCodigo(), i, 0);
            tabla.setValueAt(x.getId(), i, 1);
            tabla.setValueAt(x.getNombre(), i, 2);
            tabla.setValueAt(x.getCorreo(), i, 3);
            tabla.setValueAt(x.getNum(), i, 4);
            tabla.setValueAt(x.getSemestre(), i, 5);
            i++;
        }
        this.jTable1.setModel(tabla); // Establecer el modelo de tabla en jTable1
    }

    // Método para listar los artículos en la tabla #2
    public void ListarArticulos2() {
        DefaultTableModel tabla2 = new DefaultTableModel();
        DatoNotas objart = new DatoNotas();
        ArrayList<DatoNotas> lista3 = new ArrayList();
        lista3 = objart.Listacalificaiones();
        tabla2.addColumn("Codigo");
        tabla2.addColumn("Quiz");
        tabla2.addColumn("Talleres");
        tabla2.addColumn("Parcial");
        tabla2.setRowCount(lista3.size());
        int i = 0;
        for (DatoNotas x : lista3) {
            tabla2.setValueAt(x.getCodigo(), i, 0);
            tabla2.setValueAt(x.getQuiz(), i, 1);
            tabla2.setValueAt(x.getTalleres(), i, 2);
            tabla2.setValueAt(x.getParcial(), i, 3);
            i++;
        }
        this.jTable2.setModel(tabla2); // Establecer el modelo de tabla en jTable2
    }

