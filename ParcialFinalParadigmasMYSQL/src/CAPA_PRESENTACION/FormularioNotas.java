//Boton para eliminar notas
    private void JBTeliminar2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBTeliminar2ActionPerformed

    // Obtener los valores de los campos de texto
    String codigo = this.txtquiz.getText();
    String codigo1 = this.txttalleres.getText();
    String codigo2 = this.txtparcial.getText();
    String codigo3 = this.txtcodigo2.getText();

    // Comprobar si alguno de los campos está vacío
    if (codigo.isEmpty() || codigo1.isEmpty() || codigo2.isEmpty() || codigo3.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Por favor, seleccione notas para Eliminar.");
        return; // Terminar el método si los campos están vacíos
    }

    // Crear una instancia de la clase DatoNotas
    DatoNotas objart = new DatoNotas();

    // Mostrar un cuadro de diálogo de confirmación para eliminar notas
    int Res = JOptionPane.showConfirmDialog(null, "¿Está seguro de eliminar las notas del estudiante: " + this.txtcodigo2.getText() + "?");
    if (Res == 0) { // Si el usuario confirma la eliminación
        // Establecer el código de las notas a eliminar
        objart.setCodigo(Integer.parseInt(this.txtcodigo2.getText()));

        // Mostrar un mensaje con el resultado de la eliminación y actualizar la tabla
        JOptionPane.showMessageDialog(null, objart.EliminarCalificaciones());
        ListarArticulos2();
        JOptionPane.showMessageDialog(null, "Notas Eliminadas");

        // Limpiar los campos de texto después de la eliminación
        this.LimpiarCajasTexto();
    }

    }//GEN-LAST:event_JBTeliminar2ActionPerformed
