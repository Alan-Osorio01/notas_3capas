//Boton para eliminar estudiante
    private void JBTeliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBTeliminarActionPerformed
// Obtener los valores de los campos de texto
        String codigo = this.txtcodigo.getText();
        String codigo1 = this.txtnombre.getText();
        String codigo2 = this.txtcorreo.getText();
        String codigo3 = this.txtnumero.getText();
        String codigo4 = this.txtsemestre.getText();
        String codigo5 = this.txtid.getText();

// Comprobar si alguno de los campos de datos está vacío
        if (codigo.isEmpty() || codigo1.isEmpty() || codigo2.isEmpty() || codigo3.isEmpty() || codigo4.isEmpty() || codigo5.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, seleccione un estudiante para Eliminar.");
            return; // Terminar el método si algún campo de datos está vacío
        }

// Crear una instancia de la clase DatoNotas
        DatoNotas objart = new DatoNotas();

// Mostrar un cuadro de diálogo de confirmación para eliminar al estudiante
        int Res = JOptionPane.showConfirmDialog(null, "¿Está seguro de eliminar el estudiante: " + this.txtid.getText() + "?");
        if (Res == 0) { // Si el usuario confirma la eliminación
            objart.setId(Integer.parseInt(this.txtid.getText())); // Establecer el ID del estudiante en la instancia
            JOptionPane.showMessageDialog(null, objart.EliminarDatosBasicos()); // Eliminar los datos básicos del estudiante
            ListarArticulos(); // Actualizar la tabla de estudiantes
            JOptionPane.showMessageDialog(null, "Estudiante Eliminado"); // Mostrar un mensaje de confirmación
            this.LimpiarCajasTexto(); // Limpiar los campos de texto
        }

    }//GEN-LAST:event_JBTeliminarActionPerformed

